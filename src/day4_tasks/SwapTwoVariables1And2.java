package day4_tasks;

public class SwapTwoVariables1And2 {
    public static void main(String[] args) {

        int x = 10;
        int y = 15;
        int z = x;



        x = y;
        y = z;

// x = 15
// y = 10
        System.out.println(x);
        System.out.println(y);
        System.out.println("---------------");




    }
}
