package JavaCodePractice;

public class swapNumbers {
    public static void main(String[] args) {

        int x = 10;
        int y = 5;

        // need swap numbers so we should get x = 5 and y = 10

        x = x - y; //5
        y = x + y; // 10

        System.out.println("y = " + y);
        System.out.println("x = " + x);
    }
}
