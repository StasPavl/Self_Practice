package day06_tasks;

public class WeekDays {
    public static void main(String[] args) {

        int number = 1;

        String result = "";

        if(number == 1){
            result = "Monday";
        }
        if(number == 2){
            result = "Tuesday";
        }
        if(number == 3){
            result = "Wednesday";
        }
        if(number == 4){
            result = "Thursday";
        }
        if(number == 5){
            result = "Friday";
        }
        if(number == 6){
            result = "Saturday";
        }
        if(number == 7){
            result = "Sunday";
        }
        System.out.println(result);
    }
}
